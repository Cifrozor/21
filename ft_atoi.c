int		ft_atoi(char *str)
{
	int znak;
	int z;
	int i;

	znak = 1;
	z = 0;
	i = 0;
	while (str[i] == '\0' || str[i] == ' ' || str[i] == '\n' \
			|| str[i] == '\t' \
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
	{
		znak = -znak;
		i++;
	}
	if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		z = z * 10 + (str[i] - '0');
		i++;
	}
	return (z * znak);
}
